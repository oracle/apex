                       highlights = '<ul>'||l_highlights||'</ul>'
                 where id = l_ai_id;
                 commit;
/*
                -- remove previous summary as it is being replaced (generated within the g_hrs_to_replace window)
                if l_id_to_remove is not null then
                   delete from sp_project_ai_summaries
                    where id = l_id_to_remove;
                end if;
*/
            exception 
                when others then 
                    p_error_yn := 'Y';
                    sp_util.add_error_log (
                        p_package_name   => 'sp_summary_util',
                        p_procedure_name => 'generate_release_summary', 
                        p_error          => sqlerrm, 
                        p_arg1_name      => 'p_release_id', p_arg1_val => p_release_id, 
                        p_arg2_name      => 'l_ai_id',      p_arg2_val => l_ai_id );
            end;
        end if;

    else
        sp_util.add_error_log (
            p_package_name   => 'sp_summary_util',
            p_procedure_name => 'generate_release_summary', 
            p_error          => 'summarize_release_project returned without summary', 
            p_arg1_name      => 'p_release_id', p_arg1_val => p_release_id );
    end if;

end generate_release_summary;


