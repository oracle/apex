                        sp_util.get_ai_prompt (p_static_id => 'RELEASE_SUMMARY');

    summarize_release (
        p_release_id    => p_release_id,
        p_summary       => l_release_details);

    if l_release_details is not null then

        for c1 in (
            select id
              from sp_release_ai_summaries
             where release_id = p_release_id
               and created > sysdate - g_hrs_to_replace/24
             order by created desc
        ) loop
            l_id_to_remove := c1.id;
            exit;  -- exit to just get the last one, in case more than one in the delete range
